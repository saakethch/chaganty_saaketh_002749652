    private void populateBranches() {
        branchTable.setRowCount(0);

        ArrayList<Branch> branches = this.business.getBranches();

        if (branches.size() > 0) {
            for (Branch br : branches) {
                Object[] row = new Object[4];
                row[0] = br.getName();
                row[1] = br.getLibrary().getLib_building();
                row[2] = br.getLibrary().getLibrarian() == null ? "N/A" : br.getLibrary().getLibrarian().getEmp_name();
                row[3] = br.getLibrary().getManager() == null ? "N/A" : br.getLibrary().getManager().getEmp_name();
                branchTable.addRow(row);
            }
        }
    }

    private void populateUsers() {
        userTable.setRowCount(0);

        ArrayList<UserAccount> uas = this.business.getUserAccounts().getUseraccountList();
        ArrayList<Branch> branches = this.business.getBranches();
        if (uas.size() > 0) {
            for (UserAccount ua : uas) {
                Object[] row = new Object[3];
                row[0] = ua.getAccountId();
                row[1] = ua.getUsername();
                row[2] = ua.getRole();
                userTable.addRow(row);
            }

            for (Branch br : branches) {
                ArrayList<UserAccount> uasb = br.getBranchUad().getUseraccountList();
                for (UserAccount ua : uasb) {
                    Object[] row = new Object[3];
                    row[0] = ua.getAccountId();
                    row[1] = ua.getUsername();
                    row[2] = ua.getRole();
                    userTable.addRow(row);
                }
            }
        }
    }

