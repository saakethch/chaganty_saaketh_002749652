    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:

        // based on role selected, we will register the person into those directories
        // remember to check if the credentials are unique
        if (jComboBox1.getSelectedItem().equals("customer")) {
            UserAccount customer = this.business.getUserAccounts().createUserAccount(username.getText(), password.getText(), new CustomerRole());
            if (customer == null) {
                JOptionPane.showMessageDialog(null, "Username already exists");
            } else {
                this.business.addCustomer(customer.getAccountId(), name.getText());
                JOptionPane.showMessageDialog(null, "Added Customer");
                populateUsers();
            }
        }

        if (jComboBox1.getSelectedItem().equals("manager")) {
            String bName = fieldBranch.getText();
            if (bName != "") {
                UserAccount emp = this.branch.getBranchUad().createUserAccount(username.getText(), password.getText(), new BranchAdminRole());
                if (emp == null) {
                    JOptionPane.showMessageDialog(null, "Username already exists");
                } else {
                    this.business.findBranchByName(bName).getLibrary().addEmployee(emp.getAccountId(), name.getText(), "manager",
                            Integer.parseInt(exp.getText()), this.branch.getLibrary());
                    JOptionPane.showMessageDialog(null, "Added Branch manager");
                    populateUsers();
                    populateBranches();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Create a new branch by filling the branch field to add it's branch manager.");
            }
        }
        if (jComboBox1.getSelectedItem().equals("librarian")) {
            String bName = fieldBranch.getText();
            if (bName != "") {
                UserAccount emp = this.branch.getBranchUad().createUserAccount(username.getText(), password.getText(), new LibrarianRole());
                if (emp == null) {
                    JOptionPane.showMessageDialog(null, "Username already exists");
                } else {
                    this.business.findBranchByName(bName).getLibrary().addEmployee(emp.getAccountId(), name.getText(), "librarian",
                            Integer.parseInt(exp.getText()), this.branch.getLibrary());
                    JOptionPane.showMessageDialog(null, "Added Librarian");
                    populateUsers();
                    populateBranches();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Create a new branch by filling the branch field to add it's librarian.");
            }
        }
    }//GEN-LAST:event_registerBtnActionPerformed

